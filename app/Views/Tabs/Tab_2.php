    <h3>Turing e sua Máquina</h3>
      <div class= "mb-4">
          <p class= "mb-4" >
              Alan Turing foi um incrível matemático, lógico e criptoanalista, cujos estudos e projetos se tornaram base para a tecnologia atual e ele é o motivo de você estar usando um computador neste momento.
            <br>
              O primeiro destaque da vida de Turing veio quando ele estava perto dos 30 anos. Trabalhando em conjunto com uma organização inglesa, o matemático foi capaz de criar um sistema para traduzir os textos encriptados pelos alemães chamado “bombe”. Sua máquina era extremamente eficaz contra o equipamento inimigo, que usava uma encriptadora chamada Enigma para fazer com que as mensagens captadas pelos britânicos não fossem compreensíveis.
              A bombe captava e identificava quando o sinal estava protegido pelo mesmo padrão da Enigma, para depois usar um padrão de lógica que ignorava informações que se contradiziam e gerar a mensagem verdadeira.
          </p>
          <h4>A Máquina</h4>
            <p>
            A ideia do equipamento é bastante simples: o aparelho deve ser capaz de manipular símbolos em uma fita de acordo com uma série de regras para guardar informações. É assim que todos os computadores funcionam.
